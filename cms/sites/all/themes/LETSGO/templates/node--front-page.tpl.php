        <h1> Wat doen we? </h1>
        <p>Het LETS- systeem is automatisch een gesloten systeem dat alleen door de leden gebruikt kan worden.
            Enkel de LETS-leden hebben afgesproken om met elkaar te handelen en de transacties te waarderen met LETS-eenheden.
            Voor mensen buiten het LETS-systeem hebben LETS-eenheden geen waarde. Ze zullen ze niet accepteren omdat ze er niets
            mee zijn. LETS-leden ruilen onderling hun aanbiedingen en vragen uit. De LETS-groep organiseert enkel het kader
            waarbinnen het ruilen zich kan ontplooien.
            Zij stelt de middelen ter beschikking waarbinnen leden met elkaar tot uitwisselingen komen.</p>
        <h1>Wat is LETS?</h1>
        <p>LETS staat voor "Local Exchange and Trading System"
            Vrij vertalen we dit wel eens als "lokaal uitwisselings systeem". Het ruilsysteem in deze vorm ontstond in een plaats
            in Canada waar op korte termijn de werkverschaffing en dus rechtstreeks de inkomsten van mensen wegtrok.
            Uit noodzaak gingen zij weer ruilen, maar dan wel op een eigentijdse manier. Nadien heeft het idee zich snel verspreid
            over de hele wereld.
        </p>
        <h1>Credits</h1>
        <p>Via de LETS-eenheid kan je met elkaar handelen en transacties waarderen. Dit gebeurd via een formulier record transaction.
        </p>
        <h1>Diensten</h1>
        <p> Bij het tabblad artikelen kan je alle diensten terug vinden die er beschikbaar zijn. Ook zijn die artikelen mooi gesorteerd via het tabblad groepen.
            Daar staan alle groepen waaronder de artikelen vallen.
        </p>
        <h1>Consumeren</h1>
        <p> Je kan via artikelen of via de groepen op de link van een dienst klikken en daar met een comment op reageren. Zo kom je in contact met de beheerder van deze dienst.
        </p>
        <h1>Registratieprocedure</h1>
        <p> Als je nog geen account hebt kan je via de link ,onderin het login formulier, klikken op new account.
            Daar zal je een username + je email-adres moeten doorgeven. Wij sturen jou dan een mail als we jou aanvaarden of niet.
            Nadien kan je jou inschrijven in groepen (mits toestemming van de beheerder) en kan je commentaar geven. Zelf kan je ook
            je eigen passwoord instellen om achteraf terug in te loggen.
        </p>
